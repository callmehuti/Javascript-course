# Question 1:
def sum_average(start: int, end: int) -> int:
    sumNum = 0
    while numInRange in range(start, end):
        sumNum += numInRange
        numInRange += 1
    print(sumNum)
    
# Question 2:
def sum_evens(start: int, end: int) -> int:
    sumNum = 0
    while numInRange in range(start, end):
        if (numInRange % 2) == 0:
            sumNum += numInRange
        numInRange += 1    
    print(sumNum)

# Test case 
sum_average(0,5)
sum_evens(0,5)
